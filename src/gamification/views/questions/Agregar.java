        // TODO add your handling code here:
        String question = txtQuestion.getText();
        String answer = txtAnswer.getText();
        boolean createQuestion = TemaController.createAnswerQuestion(theme,question, answer);
        if (createQuestion) {
            this.setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Cannot enter the system.\nYou might have entered an invalid or old password, or you are not allowed to log in.", "Invalid", JOptionPane.ERROR_MESSAGE);
            txtQuestion.requestFocusInWindow();
        }
        
        
